func IsChargeSkill(skillId Id) bool {
	return ifIn(skillId,
		FirePoisonArchMagicianBigBangId,
		IceLightningArchMagicianBigBangId,
		BishopBigBangId,
		EvanStage4IceBreath,
		EvanStage7FireBreath)
}

func IsShootSkillNotUsingShootingWeapon(skillId Id) bool {
	switch skillId {
	case NightLordTauntId, ShadowerTauntId, BuccaneerEnergyOrbId, DawnWarriorStage2SoulBlade, ThunderBreakerStage3Spark, ThunderBreakerStage3SharkWave, AranStage2ComboSmash, AranStage3ComboFenrir, AranStage4ComboTempest:
		return true
	default:
		return false
	}
}

func IsShootSkillNotConsumingBullet(skillId Id) bool {
	if IsShootSkillNotUsingShootingWeapon(skillId) {
		return true
	}
	switch skillId {
	case HunetrPowerKnockBackId, RangerPowerKnockBackId, HermitShadowMesoId, WindArcherStage2StormBreak, NightWalkerStage2Vampire:
		return true
	default:
		return false
	}
}

func IsKeyDownSkill(skillId Id) bool {
	return ifIn(skillId,
		FirePoisonArchMagicianBigBangId,
		IceLightningArchMagicianBigBangId,
		BishopBigBangId,
		HeroMonsterMagnetId,
		PaladinMonsterMagnetId,
		DarkKnightMonsterMagnetId,
		BowmasterHurricaneId,
		MarksmanPiercingArrowId,
		CorsairRapidFireId,
		NightWalkerStage3PoisonBomb,
		WindArcherStage3Hurricane,
		ThunderBreakerStage2CorkscrewBlow,
		EvanStage4IceBreath,
		EvanStage7FireBreath)
}

func ifIn(skillId Id, references ...Id) bool {
	for _, r := range references {
		if skillId == r {
			return true
		}
	}
	return false
}
